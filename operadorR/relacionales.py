# Operadores Relacionales

resultado = 10 < 20
print(resultado)

resultado1 = 10 > 20
print(resultado1)

resultado3 = 10 == 20
print(resultado3)

resultado4 = 10 != 20
print(resultado4)

resultado5 = 10 <= 20
print(resultado5)

resultado6 = 10 >= 20
print(resultado6)

